int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i])
	{
		if (s1[i] != s2[i])
			return (j = s1[i] - s2[i]);
		else if (s2[i] == '\0')
			return (j = s1[i]);
		i++;
	}
	return (0);
}
